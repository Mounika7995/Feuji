package november_9;

public class RelationalOperator {
	
	public boolean greaterThan(int value1,int value2) {
		boolean result=value1 > value2;
		return result;
	}
	
	public boolean lesserThan(int value1,int value2) {
		boolean result=value1 < value2;
		return result;
	}
	
	public boolean greaterThanEqual(int value1,int value2) {
		boolean result=value1 >= value2;
		return result;
	}
	
	public boolean	lesserThanEqual (int value1,int value2) {
		boolean result=value1 <= value2;
		return result;
	}
	
	public boolean	equals (int value1,int value2) {
		boolean result=value1 == value2;
		return result;
	}
	
}
